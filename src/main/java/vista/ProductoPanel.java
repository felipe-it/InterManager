    private void cargarProductos(String filtro) {
        DefaultTableModel model = (DefaultTableModel) tableProductos.getModel();
        model.setRowCount(0);
        
        List<Producto> lista = controller.listarPorUsuario(SesionUsuario.getUsuario().getId());
        lista.stream()
            .filter(p -> p.isActivo() && (filtro.isEmpty() || p.getNombre().toLowerCase().contains(filtro.toLowerCase())))
            .forEach(p -> model.addRow(new Object[]{p.getId(), p.getNombre()}));    
    }
    
    public void ordenarTabla(){ 
    TableRowSorter<TableModel> sorter = new TableRowSorter<>(tableProductos.getModel());
    tableProductos.setRowSorter(sorter);               
    }

