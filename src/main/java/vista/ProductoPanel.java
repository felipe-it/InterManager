    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        String nombre = JOptionPane.showInputDialog(this, "Nombre del producto:");
            if (nombre != null && !nombre.trim().isEmpty()) {
                boolean exito = controller.crear(nombre.trim(), SesionUsuario.getUsuario().getId());
                if (exito) cargarProductos("");
           }     else JOptionPane.showMessageDialog(this, "Error al crear el producto.");
    }//GEN-LAST:event_btnNuevoActionPerformed
   
    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int fila = tableProductos.getSelectedRow();
            if (fila == -1) {
                JOptionPane.showMessageDialog(this, "Selecciona un producto para editar.");
                return;
            }
            int id = (int) tableProductos.getValueAt(fila, 0);
            String nombreActual = (String) tableProductos.getValueAt(fila, 1);
            String nuevoNombre = JOptionPane.showInputDialog(this, "Nuevo nombre:", nombreActual);
                     
            if (nuevoNombre != null && !nuevoNombre.trim().isEmpty()) {
                boolean exito = controller.actualizar(id, nuevoNombre.trim());
                if (exito) cargarProductos("");
                else JOptionPane.showMessageDialog(this, "Error al editar el producto.");
            }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tableProductos.getSelectedRow();
            if (fila == -1) {
                JOptionPane.showMessageDialog(this, "Selecciona un producto para eliminar.");
                return;
            }
            
            int id = (int) tableProductos.getValueAt(fila, 0);
            int confirm = JOptionPane.showConfirmDialog(this, "¿Seguro que deseas eliminar este producto?");
            if (confirm == JOptionPane.YES_OPTION) {
                boolean exito = controller.marcarComoInactivo(id, SesionUsuario.getUsuario().getId());
                if (exito) cargarProductos("");
                else JOptionPane.showMessageDialog(this, "No se puede eliminar: tiene transacciones asociadas.");
            }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tableProductosMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableProductosMouseEntered
        // TODO add your handling code here:
        
        JTableHeader header = tableProductos.getTableHeader();
        
        int col = header.columnAtPoint(evt.getPoint());
        
        if (col != -1) {            
            header.setCursor(Cursor.getPredefinedCursor(Cursor.HAND_CURSOR));
        } else {            
            header.setCursor(Cursor.getDefaultCursor());
        }
        
    }//GEN-LAST:event_tableProductosMouseEntered

