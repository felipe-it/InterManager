    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        String nombre = JOptionPane.showInputDialog(this, "Nombre del proveedor:");
        String email = JOptionPane.showInputDialog(this, "Email del proveedor:");
        String telefono = JOptionPane.showInputDialog(this, "Teléfono del proveedor:");
        
    if (nombre != null && email != null && telefono != null &&
        !nombre.trim().isEmpty() && !email.trim().isEmpty() && !telefono.trim().isEmpty()) {
        
        boolean exito = controller.crear(nombre.trim(), email.trim(), telefono.trim(), SesionUsuario.getUsuario().getId());
        if (exito) {
            cargarProveedores("");
            
        } else {
            JOptionPane.showMessageDialog(this, "Error al crear el proveedor.");
        }
        
    } else {
        JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
    }
    }//GEN-LAST:event_btnNuevoActionPerformed
   
    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int fila = tableProveedores.getSelectedRow();
    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Selecciona un proveedor para editar.");
        return;
    }

    int id = (int) tableProveedores.getValueAt(fila, 0);
    String nombreActual = (String) tableProveedores.getValueAt(fila, 1);
    String emailActual = (String) tableProveedores.getValueAt(fila, 2);
    String telefonoActual = (String) tableProveedores.getValueAt(fila, 3);

    JPanel panel = new JPanel(new GridLayout(3, 2, 10, 10));
            JTextField txtNombre = new JTextField(nombreActual);
            JTextField txtEmail = new JTextField(emailActual);
            JTextField txtTelefono = new JTextField(telefonoActual);         
            panel.add(new JLabel("Nombre:"));
            panel.add(txtNombre);
            panel.add(new JLabel("Email:"));
            panel.add(txtEmail);
            panel.add(new JLabel("Teléfono:"));
            panel.add(txtTelefono);                       
    
            int result = JOptionPane.showConfirmDialog(null, panel, "Modificar datos personales",
                    JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
            if (result == JOptionPane.OK_OPTION) {
                
                String nuevoNombre = txtNombre.getText();
                String nuevoEmail = txtEmail.getText();
                String nuevoTelefono = txtTelefono.getText();  

    if (nuevoNombre != null && nuevoEmail != null && nuevoTelefono != null &&
        !nuevoNombre.trim().isEmpty() && !nuevoEmail.trim().isEmpty() && !nuevoTelefono.trim().isEmpty()) {

        boolean exito = controller.actualizar(id, nuevoNombre.trim(), nuevoEmail.trim(), nuevoTelefono.trim(), true, SesionUsuario.getUsuario().getId());
        if (exito) {
            cargarProveedores("");
        } else {
            JOptionPane.showMessageDialog(this, "Error al editar el proveedor.");
        }
        
    } else {
        JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
   }}
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tableProveedores.getSelectedRow();
        if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Selecciona un proveedor para eliminar.");
        return;
        }

        int id = (int) tableProveedores.getValueAt(fila, 0);
        int confirm = JOptionPane.showConfirmDialog(this, "¿Seguro que deseas eliminar este proveedor?");
        
        if (confirm == JOptionPane.YES_OPTION) {
            boolean exito = controller.marcarComoInactivo(id, SesionUsuario.getUsuario().getId());
            if (exito) {
                cargarProveedores("");
        } else {
            JOptionPane.showMessageDialog(this, "No se puede eliminar el proveedor.");
        }
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

