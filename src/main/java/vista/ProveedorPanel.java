    private void cargarProveedores(String filtro) {
    DefaultTableModel model = (DefaultTableModel) tableProveedores.getModel();
    model.setRowCount(0);
    List<Proveedor> lista = controller.listarPorUsuario(SesionUsuario.getUsuario().getId());
    lista.stream()
        .filter(p -> p.isActivo() && (filtro.isEmpty() || p.getNombre().toLowerCase().contains(filtro.toLowerCase())))
        .forEach(p -> model.addRow(new Object[]{
            p.getId(),
            p.getNombre(),
            p.getEmail(),
            p.getTelefono()
        }));
    
}
    
    public void ordenarTabla(){
    TableRowSorter<TableModel> sorter = new TableRowSorter<>(tableProveedores.getModel());
    tableProveedores.setRowSorter(sorter);   
    
    }
